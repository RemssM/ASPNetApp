using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using ASPNetApp.Models;
using Microsoft.AspNetCore.Identity;

namespace ASPNetApp.Controllers
{
    public class ToDoController : Controller
    {
        private readonly ASPNetAppContext _context;
        public UserManager<User> UserManager { get; set; }

        public ToDoController(ASPNetAppContext context, UserManager<User> um)
        {
            _context = context;
            UserManager = um;

        }

        // GET: ToDoes
        public async Task<IActionResult> Index(string Importance, string searchString)
        {
            var current_User = UserManager.GetUserAsync(HttpContext.User).Result;
            // Use LINQ to get list of genres.
            IQueryable<string> genreQuery = from m in _context.ToDo
                                            orderby m.Importance
                                            select m.Importance;

            var toDo = from m in _context.ToDo
                       select m;

            if(null != current_User)
            {
                toDo = toDo.Where(x => x.User == current_User);
                if(!string.IsNullOrEmpty(searchString))
                {
                    toDo = toDo.Where(s => s.Title.Contains(searchString));
                }

                if(!string.IsNullOrEmpty(Importance))
                {
                    toDo = toDo.Where(x => x.Importance == Importance);
                }

                var ToDoImportanceVM = new ToDoImportanceViewModel
                {
                    Importance = new SelectList(await genreQuery.Distinct().ToListAsync()),
                    toDos = await toDo.ToListAsync()
                };

                return View(ToDoImportanceVM);
            }
            return View("ErreurPasConnecte");
        }

        // GET: ToDoes/Details/5
        public async Task<IActionResult> Details(int? id)
        {
            if(id == null)
            {
                return NotFound();
            }

            var toDo = await _context.ToDo
                .FirstOrDefaultAsync(m => m.Id == id);
            if(toDo == null)
            {
                return NotFound();
            }

            return View(toDo);
        }

        // GET: ToDoes/Create
        public IActionResult Create()
        {
            return View();
        }

        // POST: ToDoes/Create
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Create([Bind("Id,Title,Date,Importance,Content,Deadline,ID_User")] ToDo toDo)
        {
            var current_User = UserManager.GetUserAsync(HttpContext.User).Result;

            if(ModelState.IsValid)
            {
                toDo.User = current_User;
                _context.Add(toDo);
                await _context.SaveChangesAsync();
                return RedirectToAction(nameof(Index));
            }
            return View(toDo);
        }

        // GET: ToDoes/Edit/5
        public async Task<IActionResult> Edit(int? id)
        {
            if(id == null)
            {
                return NotFound();
            }

            var toDo = await _context.ToDo.FindAsync(id);
            if(toDo == null)
            {
                return NotFound();
            }
            return View(toDo);
        }

        // POST: ToDoes/Edit/5
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Edit(int id, [Bind("Id,Title,Date,Importance,Content,Deadline")] ToDo toDo)
        {
            if(id != toDo.Id)
            {
                return NotFound();
            }

            if(ModelState.IsValid)
            {
                try
                {
                    _context.Update(toDo);
                    await _context.SaveChangesAsync();
                }
                catch(DbUpdateConcurrencyException)
                {
                    if(!ToDoExists(toDo.Id))
                    {
                        return NotFound();
                    }
                    else
                    {
                        throw;
                    }
                }
                return RedirectToAction(nameof(Index));
            }
            return View(toDo);
        }

        // GET: ToDoes/Delete/5
        public async Task<IActionResult> Delete(int? id)
        {
            if(id == null)
            {
                return NotFound();
            }

            var toDo = await _context.ToDo
                .FirstOrDefaultAsync(m => m.Id == id);
            if(toDo == null)
            {
                return NotFound();
            }

            return View(toDo);
        }

        // POST: ToDoes/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> DeleteConfirmed(int id)
        {
            var toDo = await _context.ToDo.FindAsync(id);
            _context.ToDo.Remove(toDo);
            await _context.SaveChangesAsync();
            return RedirectToAction(nameof(Index));
        }

        private bool ToDoExists(int id)
        {
            return _context.ToDo.Any(e => e.Id == id);
        }
    }
}
